package com.hxjf.dubei.bean;

import java.io.Serializable;
import java.util.ArrayList;

/**
 * Created by Chen_Zhang on 2017/6/27.
 * 阅读提醒
 */

public class AlarmBean implements Serializable{
    int hour;
    int minute;
    String state;
    ArrayList<Integer> weeklist;
    String weekdata;

    private static final long serialVersionUID = -7060210544600464481L;

    public void setHour(int hour) {
        this.hour = hour;
    }

    public void setMinute(int minute) {
        this.minute = minute;
    }

    public void setState(String state) {
        this.state = state;
    }

    public void setWeeklist(ArrayList<Integer> weeklist) {
        this.weeklist = weeklist;
    }

    public void setWeekdata(String weekdata) {
        this.weekdata = weekdata;
    }

    public int getHour() {
        return hour;
    }

    public int getMinute() {
        return minute;
    }

    public String getState() {
        return state;
    }

    public ArrayList<Integer> getWeeklist() {
        return weeklist;
    }

    public String getWeekdata() {
        return weekdata;
    }

    @Override
    public String toString() {
        return "AlarmBean{" +
                "hour=" + hour +
                ", minute=" + minute +
                ", state='" + state + '\'' +
                ", weeklist=" + weeklist +
                ", weekdata='" + weekdata + '\'' +
                '}';
    }
}
